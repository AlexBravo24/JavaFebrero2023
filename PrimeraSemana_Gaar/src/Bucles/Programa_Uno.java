package Bucles;
// Programa un algoritmo que realice la tabla de multiplicar del 12
public class Programa_Uno {
	public static void main(String[] args) {
		System.out.println("TABLA DE MULTIPLICAR NUMERO 12 \n\n");
		
		for(int i=1; i<=10; i++)
		{
			System.out.println("12 X "+ i + " = "+12*i);
		}
		
	}

}
