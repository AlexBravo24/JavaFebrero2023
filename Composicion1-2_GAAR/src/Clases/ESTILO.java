package Clases;

public class ESTILO {
   
	  private String estilo;
	  
	  
	  public ESTILO() {}


	public ESTILO(String estilo) {
		this.estilo = estilo;
	}


	public String getEstilo() {
		return estilo;
	}


	public void setEstilo(String estilo) {
		this.estilo = estilo;
	}


	@Override
	public String toString() {
		return "ESTILO [estilo=" + estilo + "]";
	}
	  
	  
	
	
}
