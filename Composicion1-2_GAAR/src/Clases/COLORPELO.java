package Clases;

public class COLORPELO {
	
	private String color;
	
	public COLORPELO() {}

	public COLORPELO(String color) {
		this.color = color;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	@Override
	public String toString() {
		return "COLORPELO [color=" + color + "]";
	}
	
	
	
	

}
