package Clases;

public class COLOROJOS {
	
	private String color;
 
	 public COLOROJOS() {}

	public COLOROJOS(String color) {
		this.color = color;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	@Override
	public String toString() {
		return "COLOROJOS [color=" + color + "]";
	}
	
	 

}
